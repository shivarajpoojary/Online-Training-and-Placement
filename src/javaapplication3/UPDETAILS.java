                    sql="UPDATE `student`.`criteria` SET `Crsslcperc`=?,`Crpucperc`=?,`Crdegagg`=?,`Crdegpass`=?,`Crbacklogs`=? ,`comp_descr` = ? WHERE `criteria`.`comp_ID` = ? ";
                    pst = con.prepareStatement(sql);
