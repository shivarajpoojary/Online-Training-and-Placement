    private void LOGINActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LOGINActionPerformed
        // TODO add your handling code here:
        
        if(username.getText().equals("")&&password.getText().equals(""))
       {
                   JOptionPane.showMessageDialog(null,"Username and password can't be empty");
       }
        else
        {
         try
            {
            
               
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/student","root","");
                    String sql,sql1;
                    PreparedStatement pst,pst1;
                    
                    
                    sql1 = "SELECT *FROM `admreg` WHERE username = ? AND password = ? LIMIT 0 , 30";
                    pst1 = con.prepareStatement(sql1);
                    pst1.setString(1,username.getText());
                    pst1.setString(2,password.getText());
                    ResultSet rs=pst1.executeQuery();
                     
                    if(rs.next())
                    {
                        JOptionPane.showMessageDialog(null,"You have already Registered");
                    }
                    else
                    {
                        sql = "INSERT INTO `student`.`admreg` (`username`,`password`)VALUES(?, ?)";
                        pst = con.prepareStatement(sql);
                        pst.setString(1,username.getText());
                        pst.setString(2,password.getText());
                        pst.execute();                  
                        JOptionPane.showMessageDialog(null,"Registeration Successful");
                        username.setText("");
                        password.setText("");
                        dispose();
                    }

        }
        catch(HeadlessException | ClassNotFoundException | SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);
       }
        } 
  
   
    }//GEN-LAST:event_LOGINActionPerformed

